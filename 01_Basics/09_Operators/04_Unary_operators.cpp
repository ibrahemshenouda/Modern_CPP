#include <iostream>
/*
Operators: Operatopn between one ore more variables
- For each operator:
    -Action: (built-in) or (user-defined) (operator overloding)).
    -Type: A*A R U L B
    -Result
    -no. of operands: (on or mode operand)
*/
int main()
{
    // ++ -> increment
    int a = 10;
    a++; // action: add 1 for a , a = 11
    a++;

    // -- -> decrement
    int b = 10;
    b--; // action: sub 1 from b , b = 9

    /*
    Operator: increment(++), decrement(--)
        1.Type         : Unary
        2.Action       : add or sub 1
        3.Result       : increase or decrease by 1
        4.# of operands: 1
    */
    return 0;
}